export function getCategoryBySlug(slug: string) {
  return categories.find(c => c.slug === slug);
}

export function getTagSlug(tag: string): string {
  return tag.replace(/\s+/g, "-");
}

export function getAllTags(): { name: string; slug: string; count: number }[] {
  const tagMap = new Map<string, number>();
  blogPosts.forEach(post => {
    post.tags.forEach((tag, i) => {
      const slug = post.tagSlugs[i];
      tagMap.set(slug, (tagMap.get(slug) || 0) + 1);
    });
  });
  return Array.from(tagMap.entries()).map(([slug, count]) => {
    const post = blogPosts.find(p => p.tagSlugs.includes(slug));
    const tagIndex = post?.tagSlugs.indexOf(slug) ?? 0;
    return { name: post?.tags[tagIndex] ?? slug, slug, count };
  }).sort((a, b) => b.count - a.count);
}

export function getPostHeadings(post: BlogPost): BlogHeading[] {
  return post.sections.map(s => ({
    id: s.heading.replace(/\s+/g, "-").replace(/[^\u0600-\u06FF\w-]/g, ""),
    text: s.heading,
    level: s.headingLevel,
  }));
}

export function getRelatedPosts(currentSlug: string, limit = 3): BlogPost[] {
  const current = getBlogPost(currentSlug);
  if (!current) return [];
  const scored = blogPosts
    .filter(p => p.slug !== currentSlug)
    .map(p => {
      let score = 0;
      if (p.categorySlug === current.categorySlug) score += 3;
      p.tagSlugs.forEach(t => { if (current.tagSlugs.includes(t)) score += 1; });
      return { post: p, score };
    })
    .sort((a, b) => b.score - a.score);
  return scored.slice(0, limit).map(s => s.post);
}

export function getPostsByCategory(categorySlug: string): BlogPost[] {
  return blogPosts.filter(p => p.categorySlug === categorySlug);
}

export function getPostsByTag(tagSlug: string): BlogPost[] {
  return blogPosts.filter(p => p.tagSlugs.includes(tagSlug));
}

export function getWordCount(post: BlogPost): number {
  let count = 0;
  post.sections.forEach(s => {
    count += s.heading.split(/\s+/).length;
    s.content.forEach(p => { count += p.split(/\s+/).length; });
  });
  post.faqs.forEach(f => {
    count += f.question.split(/\s+/).length;
    count += f.answer.split(/\s+/).length;
  });
  return count;
}

